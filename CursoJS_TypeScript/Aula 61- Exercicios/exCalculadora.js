//Crie uma calculadora que seja capaz de realizar operações básicas como adição, subtração, multiplicação e divisão.
function calcAdic (x , y){
     return x + y;
}
function calcSub(x , y){
     return x - y;
}
function calcDiv(x, y){
     return x / y;
}
function calcMult (x, y){
     return x * y;
}

console.log(calcAdic(2,10));
console.log(calcDiv( 10,2));
console.log(calcMult(2,8));
